class Solution {
  public:
    // Function returns the second
    // largest elements
    int getSecondLargest(vector<int> &arr) {
        
        int maxi = -1;
        int ans = -1;
        for(auto i : arr){
            if(i>maxi){
                ans = maxi;
                maxi = i;
            }
            else if(i>ans && i<maxi)
                ans = i;
        }
        
        return ans;
        
    }
};

