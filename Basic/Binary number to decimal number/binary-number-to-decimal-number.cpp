class Solution {
  public:
    int binary_to_decimal(string str) {
        
        int decimal=0;
        for(auto i: str){
            decimal=(decimal<<1) | (i-'0');
        }
        return decimal;
        
    }
};

//{ Driver Code Starts.
int main() {
    int T;
    cin >> T;
    while (T--) {
        string str;
        cin >> str;
        Solution ob;
        int ans = ob.binary_to_decimal(str);
        cout << ans << "\n";
    }
    return 0;
}
